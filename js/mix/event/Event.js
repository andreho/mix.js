mix.declare("mix.event.Event", function()
{
	"use strict";
	//-----------------------------------------------------------------------------------------------------------------------
	function Event(type, target)
	{
		this.type = type;
		this.target = target;
	}

	//-----------------------------------------------------------------------------------------------------------------------
	Event.create = function create(type, target)
	{
		return new Event(type, target);
	};
	//-----------------------------------------------------------------------------------------------------------------------
	return Event;
});
